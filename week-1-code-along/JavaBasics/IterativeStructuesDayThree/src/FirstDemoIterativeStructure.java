
public class FirstDemoIterativeStructure {

	public static void main(String[] args) {
		
		int count=0;
		while(count<3) {
			count++;
			System.out.println("This is a loop");
		}
		
		int count2=10;
		while(count2<=30) {
			count2=count2+10;
			System.out.println("This is another loop");
		}
		
		int count3=3;
		while(count3>0) {
			count3--;
			System.out.println("This is a third loop");
		}
	}

}
